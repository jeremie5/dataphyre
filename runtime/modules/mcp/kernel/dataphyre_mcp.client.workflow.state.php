	/**
	 * Audits a workflow state payload for structure, phase, and handoff risks.
	 *
	 * @param array<string,mixed> $args Workflow state and audit options.
	 * @return array Workflow state audit payload.
	 */
	private function mcp_workflow_state_audit(array $args): array {
		$expected_workflow=strtolower(trim((string)($args['workflow'] ?? 'generic')));
		if(!in_array($expected_workflow, ['feature', 'routes', 'sql', 'diagnostics', 'client', 'release', 'generic'], true)){
			$expected_workflow='generic';
		}
		$state=[];
		$parse_error='';
		if(isset($args['state']) && is_array($args['state'])){
			$state=$args['state'];
		}
		elseif(trim((string)($args['state_json'] ?? ''))!==''){
			$decoded=json_decode((string)$args['state_json'], true);
			if(is_array($decoded)){
				$state=$decoded;
			}
			else{
				$parse_error='state_json could not be decoded as an object.';
			}
		}
		$findings=[];
		$warnings=[];
		if($state===[]){
			$findings[]=['severity'=>'error', 'code'=>'missing_state', 'message'=>'Provide state as an object or state_json as a JSON object.'];
		}
		$workflow=(string)($state['workflow'] ?? '');
		if($expected_workflow!=='generic' && $workflow!=='' && $workflow!==$expected_workflow){
			$findings[]=['severity'=>'warning', 'code'=>'workflow_mismatch', 'message'=>'Workflow state does not match the expected workflow argument.'];
		}
		if($workflow==='' && $state!==[]){
			$findings[]=['severity'=>'warning', 'code'=>'missing_workflow', 'message'=>'Workflow state should include a workflow label.'];
		}
		foreach(['state_id', 'workflow', 'task', 'created_at', 'updated_at', 'client', 'current_phase', 'last_decision', 'pending_tools', 'completed_tools'] as $field){
			if(!array_key_exists($field, $state)){
				$findings[]=['severity'=>'warning', 'code'=>'missing_'.$field, 'message'=>'Workflow state is missing '.$field.'.'];
			}
		}
		$allowed_phases=['start', 'choose_workflow', 'pre_run_handoff', 'client_run', 'capture', 'audit', 'checkpoint', 'handoff_summary', 'verify', 'done'];
		$current_phase=(string)($state['current_phase'] ?? '');
		if($current_phase!=='' && !in_array($current_phase, $allowed_phases, true)){
			$findings[]=['severity'=>'warning', 'code'=>'unknown_current_phase', 'message'=>'current_phase should be one of the exported state schema phases.'];
		}
		$allowed_decisions=['start_workflow', 'review_transcript', 'fix_blocked_transcript', 'summarize_or_verify', 'done'];
		$last_decision=(string)($state['last_decision'] ?? '');
		if($last_decision!=='' && !in_array($last_decision, $allowed_decisions, true)){
			$findings[]=['severity'=>'warning', 'code'=>'unknown_last_decision', 'message'=>'last_decision should be one of the exported state schema decisions.'];
		}
		$allowed_checkpoint_statuses=['', 'empty', 'healthy', 'needs_review', 'blocked', 'unknown'];
		$checkpoint_status=(string)($state['checkpoint_status'] ?? '');
		if(!in_array($checkpoint_status, $allowed_checkpoint_statuses, true)){
			$findings[]=['severity'=>'warning', 'code'=>'unknown_checkpoint_status', 'message'=>'checkpoint_status should be empty, healthy, needs_review, blocked, or unknown.'];
		}
		$tool_names=array_map(static fn(array $tool): string => (string)($tool['name'] ?? ''), $this->list_tools()['tools']);
		$tool_refs=[];
		foreach(['last_tool', 'pending_tools', 'completed_tools'] as $field){
			$value=$state[$field] ?? null;
			if(is_string($value) && $value!==''){
				$tool_refs[]=$value;
			}
			elseif(is_array($value)){
				foreach($value as $tool){
					if(is_string($tool) && $tool!==''){
						$tool_refs[]=$tool;
					}
				}
			}
			elseif(array_key_exists($field, $state) && $value!==null){
				$findings[]=['severity'=>'warning', 'code'=>'invalid_'.$field, 'message'=>$field.' should be a string or array of tool names.'];
			}
		}
		$unknown_tools=array_values(array_unique(array_filter($tool_refs, static fn(string $tool): bool => !in_array($tool, $tool_names, true))));
		if($unknown_tools!==[]){
			$findings[]=['severity'=>'warning', 'code'=>'unknown_tools', 'message'=>'Workflow state references tools that are not registered.', 'tools'=>$unknown_tools];
		}
		foreach(['pending_tools', 'completed_tools', 'findings', 'notes'] as $field){
			if(array_key_exists($field, $state) && !is_array($state[$field])){
				$findings[]=['severity'=>'warning', 'code'=>'invalid_'.$field, 'message'=>$field.' should be an array.'];
			}
		}
		$serialized=json_encode($state, JSON_UNESCAPED_SLASHES) ?: '';
		$sensitive_key=$this->mcp_sensitive_assignment_key_pattern();
		$secret_patterns=[
			'secret_assignment'=>'/\b(?:'.$sensitive_key.')\b\s*(?:=>|:|=)\s*["\']?[^"\'\s\[\{]+/i',
			'bearer_token'=>'/Bearer\s+[A-Za-z0-9._~+\/=-]{12,}/i',
			'connection_string'=>'/\b(?:mysql|pgsql|postgres|redis|mongodb):\/\/[^"\s]+/i',
			'absolute_windows_path'=>'/[A-Za-z]:(?:\\\\|\/)[^"\n\r]+/',
			'signed_url_parameter'=>'/\b(?:X-Amz-Signature|X-Amz-Credential|X-Amz-Security-Token|AWSAccessKeyId|Signature|Expires|sig|signature|passkey|totp|access_token|id_token|refresh_token)=([^\s&#"\'<>]+)/i',
			'scoped_identifier'=>'/\b(?:tenant|tenant_id|tenantId|product|product_id|productId|customer|customer_id|customerId|account|account_id|accountId)\b\s*(?::|=|=>)\s*["\']?[^\s<>"\'\[\{]+/i',
		];
		foreach($secret_patterns as $code=>$pattern){
			if(preg_match($pattern, $serialized)===1){
				$warnings[]=$code;
			}
		}
		if($warnings!==[]){
			$findings[]=['severity'=>'error', 'code'=>'redaction_risk', 'message'=>'Workflow state contains secret-looking or machine-local content that should be redacted before sharing.', 'signals'=>array_values(array_unique($warnings))];
		}
		if($parse_error!==''){
			$findings[]=['severity'=>'error', 'code'=>'parse_error', 'message'=>$parse_error];
		}
		$error_count=count(array_filter($findings, static fn(array $finding): bool => ($finding['severity'] ?? '')==='error'));
		$warning_count=count(array_filter($findings, static fn(array $finding): bool => ($finding['severity'] ?? '')==='warning'));
		return [
			'audit_type'=>'dataphyre_mcp_workflow_state_audit',
			'write_policy'=>'read_only',
			'execution'=>'not_executed',
			'workflow'=>$workflow!=='' ? $workflow : $expected_workflow,
			'expected_workflow'=>$expected_workflow,
			'protocol'=>'2025-11-25',
			'passed'=>$error_count===0,
			'error_count'=>$error_count,
			'warning_count'=>$warning_count,
			'state_id'=>$this->mcp_safe_handoff_label($state['state_id'] ?? ''),
			'current_phase'=>$current_phase,
			'last_decision'=>$last_decision,
			'pending_tool_count'=>count(is_array($state['pending_tools'] ?? null) ? $state['pending_tools'] : []),
			'completed_tool_count'=>count(is_array($state['completed_tools'] ?? null) ? $state['completed_tools'] : []),
			'findings'=>$findings,
			'redaction_signals'=>array_values(array_unique($warnings)),
			'follow_up_tools'=>[
				'dataphyre_mcp_workflow_state_schema_export',
				'dataphyre_mcp_workflow_next_action_export',
				'dataphyre_mcp_safety_boundary_report',
			],
			'application_agent_operating_contract'=>$this->mcp_application_agent_operating_contract('workflow_state_audit'),
			'ordinary_app_work'=>$this->mcp_ordinary_app_work_contract('workflow_state_audit'),
			'usage_notes'=>[
				'This audit inspects the provided client-owned state shape only; it does not persist, mutate, or resume state.',
				'Fix error findings before sharing workflow state across agents or storing it in fixtures.',
				'Use checkpoint and transcript references rather than raw response bodies or machine-local paths.',
			],
		];
	}

	/**
	 * Builds a compact summary from a workflow state payload.
	 *
	 * @param array<string,mixed> $args Workflow state and summary options.
	 * @return array Workflow state summary payload.
	 */
	private function mcp_workflow_state_summary_export(array $args): array {
		$workflow=strtolower(trim((string)($args['workflow'] ?? 'generic')));
		if(!in_array($workflow, ['feature', 'routes', 'sql', 'diagnostics', 'client', 'release', 'generic'], true)){
			$workflow='generic';
		}
		$state=[];
		if(isset($args['state']) && is_array($args['state'])){
			$state=$args['state'];
		}
		elseif(trim((string)($args['state_json'] ?? ''))!==''){
			$decoded=json_decode((string)$args['state_json'], true);
			if(is_array($decoded)){
				$state=$decoded;
			}
		}
		$audit=$this->mcp_workflow_state_audit([
			'workflow'=>$workflow,
			'state'=>$state,
			'state_json'=>$args['state_json'] ?? '',
		]);
		$pending_tools=array_values(array_slice($this->mcp_workflow_app_tools(is_array($state['pending_tools'] ?? null) ? $state['pending_tools'] : []), 0, 12));
		$completed_tools=array_values(array_slice(is_array($state['completed_tools'] ?? null) ? $state['completed_tools'] : [], 0, 12));
		$notes=[];
		foreach(array_slice(is_array($state['notes'] ?? null) ? $state['notes'] : [], 0, 6) as $note){
			$notes[]=$this->redact_sensitive_text(substr((string)$note, 0, 300));
		}
		$findings=[];
		foreach(array_slice(is_array($state['findings'] ?? null) ? $state['findings'] : [], 0, 8) as $finding){
			$findings[]=$this->redact_sensitive_text(substr(is_array($finding) ? json_encode($finding, JSON_UNESCAPED_SLASHES) : (string)$finding, 0, 500));
		}
		$audit_passed=($audit['passed'] ?? false)===true;
		return [
			'export_type'=>'dataphyre_mcp_workflow_state_summary_export',
			'write_policy'=>'read_only',
			'execution'=>'not_executed',
			'workflow'=>(string)($audit['workflow'] ?? $workflow),
			'protocol'=>'2025-11-25',
			'state_id'=>$this->mcp_safe_handoff_label($state['state_id'] ?? ''),
			'task_summary'=>$this->mcp_safe_handoff_label($state['task'] ?? '', 500),
			'current_phase'=>(string)($audit['current_phase'] ?? ''),
			'last_decision'=>(string)($audit['last_decision'] ?? ''),
			'checkpoint_status'=>(string)($state['checkpoint_status'] ?? ''),
			'transcript_ref'=>$this->mcp_safe_handoff_label($state['transcript_ref'] ?? ''),
			'checkpoint_ref'=>$this->mcp_safe_handoff_label($state['checkpoint_ref'] ?? ''),
			'audit_passed'=>$audit_passed,
			'audit_error_count'=>(int)($audit['error_count'] ?? 0),
			'audit_warning_count'=>(int)($audit['warning_count'] ?? 0),
			'pending_tools'=>$pending_tools,
			'completed_tools'=>$completed_tools,
			'pending_tool_count'=>count($pending_tools),
			'completed_tool_count'=>count(is_array($state['completed_tools'] ?? null) ? $state['completed_tools'] : []),
			'state_findings'=>$findings,
			'notes'=>$notes,
			'audit_findings'=>$audit['findings'] ?? [],
			'agent_handoff'=>[
				'status_line'=>'Workflow '.((string)($audit['workflow'] ?? $workflow)).' state '.($audit_passed ? 'passed audit' : 'needs review').'.',
				'safe_to_share'=>$audit_passed,
				'next_tools'=>$audit_passed
					? $this->mcp_workflow_app_tools(array_merge($pending_tools, ['dataphyre_mcp_workflow_next_action_export']))
					: ['dataphyre_mcp_workflow_state_audit', 'dataphyre_mcp_workflow_state_schema_export', 'dataphyre_mcp_safety_boundary_report'],
				'next_tool_boundaries'=>$this->mcp_workflow_tool_boundaries($audit_passed
					? $this->mcp_workflow_app_tools(array_merge($pending_tools, ['dataphyre_mcp_workflow_next_action_export']))
					: ['dataphyre_mcp_workflow_state_audit', 'dataphyre_mcp_workflow_state_schema_export', 'dataphyre_mcp_safety_boundary_report']),
				'copy_safe_resume'=>[
					'status'=>'copy_safe_resume_ready',
					'fields'=>['workflow', 'state_id', 'task_summary', 'current_phase', 'last_decision', 'checkpoint_status', 'pending_tools', 'completed_tools', 'state_findings', 'notes', 'next_tools'],
					'use'=>'Copy these summary fields for agent handoff; keep raw workflow state and transcripts client-owned.',
					'not_included'=>['raw state JSON', 'raw transcript bodies', 'raw response bodies', 'secrets', 'tenant/customer identifiers', 'machine-local absolute paths'],
				],
			],
			'application_agent_operating_contract'=>$this->mcp_application_agent_operating_contract('workflow_state_summary'),
			'ordinary_app_work'=>$this->mcp_ordinary_app_work_contract('workflow_state_summary'),
			'publication_validation'=>$this->mcp_publication_validation_contract('workflow_state_summary'),
			'usage_notes'=>[
				'This summary is derived from client-provided workflow state; it does not persist, mutate, or resume server state.',
				'Share this summary instead of raw state when handing progress to another agent.',
				'If audit_passed is false, fix state shape or redaction issues before using the handoff.',
				'Use publication_validation only for MCP/release-surface workflow claims.',
			],
		];
	}

	/**
	 * Describes a workflow state transition between lifecycle phases.
	 *
	 * @param array<string,mixed> $args Current state and requested transition.
	 * @return array Workflow transition payload.
	 */
	private function mcp_workflow_state_transition_export(array $args): array {
		$workflow=strtolower(trim((string)($args['workflow'] ?? 'generic')));
		if(!in_array($workflow, ['feature', 'routes', 'sql', 'diagnostics', 'client', 'release', 'generic'], true)){
			$workflow='generic';
		}
		$state=[];
		if(isset($args['state']) && is_array($args['state'])){
			$state=$args['state'];
		}
		elseif(trim((string)($args['state_json'] ?? ''))!==''){
			$decoded=json_decode((string)$args['state_json'], true);
			if(is_array($decoded)){
				$state=$decoded;
			}
		}
		$summary=$this->mcp_workflow_state_summary_export([
			'workflow'=>$workflow,
			'state'=>$state,
			'state_json'=>$args['state_json'] ?? '',
		]);
		$next=$this->mcp_workflow_next_action_export([
			'task'=>$args['task'] ?? ($state['task'] ?? ''),
			'workflow'=>$workflow,
			'state'=>$state,
			'state_json'=>$args['state_json'] ?? '',
		]);
		$decision=(string)($next['decision'] ?? 'start_workflow');
		$recommended_tool=(string)($next['recommended_tool'] ?? 'dataphyre_mcp_task_start_pack_export');
		$phase_map=[
			'start_workflow'=>'pre_run_handoff',
			'review_transcript'=>'audit',
			'fix_blocked_transcript'=>'audit',
			'summarize_or_verify'=>'verify',
			'done'=>'done',
		];
		$next_phase=$phase_map[$decision] ?? 'choose_workflow';
		if($recommended_tool==='dataphyre_mcp_workflow_state_summary_export'){
			$next_phase='handoff_summary';
		}
		$current_pending=$this->mcp_workflow_app_tools(is_array($state['pending_tools'] ?? null) ? $state['pending_tools'] : []);
		$current_completed=is_array($state['completed_tools'] ?? null) ? $state['completed_tools'] : [];
		$last_tool=(string)($state['last_tool'] ?? '');
		if($last_tool!=='' && !in_array($last_tool, $current_completed, true)){
			$current_completed[]=$last_tool;
		}
		$pending_tools=$this->mcp_workflow_app_tools(array_merge([$recommended_tool], $current_pending));
		$completed_tools=array_values(array_unique(array_filter($current_completed, static fn(string $tool): bool => $tool!=='')));
		$patch=[
			'workflow'=>(string)($summary['workflow'] ?? $workflow),
			'current_phase'=>$next_phase,
			'last_decision'=>$decision,
			'last_tool'=>$recommended_tool,
			'pending_tools'=>array_values(array_slice($pending_tools, 0, 12)),
			'completed_tools'=>array_values(array_slice($completed_tools, 0, 12)),
			'checkpoint_status'=>(string)($summary['checkpoint_status'] ?? ($state['checkpoint_status'] ?? 'unknown')),
		];
		if(trim((string)($state['task'] ?? ''))==='' && trim((string)($args['task'] ?? ''))!==''){
			$patch['task']=$this->redact_sensitive_text(substr((string)$args['task'], 0, 500));
		}
		return [
			'export_type'=>'dataphyre_mcp_workflow_state_transition_export',
			'write_policy'=>'read_only',
			'execution'=>'not_executed',
			'workflow'=>(string)($summary['workflow'] ?? $workflow),
			'protocol'=>'2025-11-25',
			'state_id'=>$this->mcp_safe_handoff_label($state['state_id'] ?? ''),
			'audit_passed'=>($summary['audit_passed'] ?? false)===true,
			'current_phase'=>(string)($summary['current_phase'] ?? ''),
			'next_phase'=>$next_phase,
			'decision'=>$decision,
			'recommended_tool'=>$recommended_tool,
			'suggested_patch'=>$patch,
			'next_action'=>$next,
			'summary'=>[
				'task_summary'=>$summary['task_summary'] ?? '',
				'checkpoint_status'=>$summary['checkpoint_status'] ?? '',
				'pending_tool_count'=>$summary['pending_tool_count'] ?? 0,
				'completed_tool_count'=>$summary['completed_tool_count'] ?? 0,
				'audit_error_count'=>$summary['audit_error_count'] ?? 0,
				'audit_warning_count'=>$summary['audit_warning_count'] ?? 0,
			],
			'apply_policy'=>[
				'This is a suggested client-side patch only; the MCP server does not store or mutate workflow state.',
				'Clients should merge suggested_patch into their own state after reviewing audit_passed and audit findings.',
				'Clients should set updated_at themselves with their own clock and should not store secrets or raw response bodies.',
			],
			'application_agent_operating_contract'=>$this->mcp_application_agent_operating_contract('workflow_state_transition'),
			'ordinary_app_work'=>$this->mcp_ordinary_app_work_contract('workflow_state_transition'),
