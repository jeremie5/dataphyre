	/**
	 * Exports a synchronization pack for sharing workflow state across clients.
	 *
	 * @param array<string,mixed> $args Workflow state and synchronization options.
	 * @return array Workflow state sync pack payload.
	 */
	private function mcp_workflow_state_sync_pack_export(array $args): array {
		$workflow=strtolower(trim((string)($args['workflow'] ?? 'generic')));
		if(!in_array($workflow, ['feature', 'routes', 'sql', 'diagnostics', 'client', 'release', 'generic'], true)){
			$workflow='generic';
		}
		$state=[];
		if(isset($args['state']) && is_array($args['state'])){
			$state=$args['state'];
		}
		elseif(trim((string)($args['state_json'] ?? ''))!==''){
			$decoded=json_decode((string)$args['state_json'], true);
			if(is_array($decoded)){
				$state=$decoded;
			}
		}
		$base_args=[
			'workflow'=>$workflow,
			'state'=>$state,
			'state_json'=>$args['state_json'] ?? '',
			'task'=>$args['task'] ?? ($state['task'] ?? ''),
		];
		$schema=$this->mcp_workflow_state_schema_export(['workflow'=>$workflow]);
		$audit=$this->mcp_workflow_state_audit($base_args);
		$summary=$this->mcp_workflow_state_summary_export($base_args);
		$transition=$this->mcp_workflow_state_transition_export($base_args);
		$next=$this->mcp_workflow_next_action_export($base_args);
		return [
			'export_type'=>'dataphyre_mcp_workflow_state_sync_pack_export',
			'write_policy'=>'read_only',
			'execution'=>'not_executed',
			'workflow'=>(string)($summary['workflow'] ?? $workflow),
			'protocol'=>'2025-11-25',
			'state_id'=>$this->mcp_safe_handoff_label($state['state_id'] ?? ''),
			'audit_passed'=>($audit['passed'] ?? false)===true,
			'current_phase'=>(string)($summary['current_phase'] ?? ''),
			'next_phase'=>(string)($transition['next_phase'] ?? ''),
			'decision'=>(string)($next['decision'] ?? ''),
			'recommended_tool'=>(string)($next['recommended_tool'] ?? ''),
			'schema'=>[
				'allowed_phases'=>$schema['allowed_phases'] ?? [],
				'allowed_decisions'=>$schema['allowed_decisions'] ?? [],
				'required_redactions'=>$schema['required_redactions'] ?? [],
				'recommended_tools'=>$schema['recommended_tools'] ?? [],
			],
			'audit'=>$audit,
			'summary'=>$summary,
			'transition'=>$transition,
			'next_action'=>$next,
			'client_apply_order'=>[
				'Review audit.passed and audit.findings.',
				'Use summary.agent_handoff for agent-to-agent context when audit_passed is true.',
				'Merge transition.suggested_patch into client-owned state only after review.',
				'Set updated_at with the client clock and keep raw responses out of state.',
			],
			'application_agent_operating_contract'=>$this->mcp_application_agent_operating_contract('workflow_state_sync_pack'),
			'ordinary_app_work'=>$this->mcp_ordinary_app_work_contract('workflow_state_sync_pack'),
			'publication_validation'=>$this->mcp_publication_validation_contract('workflow_state_sync_pack'),
			'usage_notes'=>[
				'This sync pack is a read-only bundle; the MCP server does not persist, mutate, or resume workflow state.',
				'Use the pack when handing state across agents or client turns without sharing raw transcripts.',
				'If audit_passed is false, fix state shape or redaction issues before applying the suggested patch.',
				'Use publication_validation only for MCP/release-surface workflow claims.',
			],
		];
	}

	/**
	 * Converts workflow state history into a timeline payload.
	 *
	 * @param array<string,mixed> $args Workflow state and timeline options.
	 * @return array Workflow state timeline payload.
	 */
	private function mcp_workflow_state_timeline_export(array $args): array {
		$sync=$this->mcp_workflow_state_sync_pack_export($args);
		$allowed_phases=is_array($sync['schema']['allowed_phases'] ?? null) ? $sync['schema']['allowed_phases'] : [];
		$current_phase=(string)($sync['current_phase'] ?? '');
		$next_phase=(string)($sync['next_phase'] ?? '');
		$timeline=[];
		foreach($allowed_phases as $index=>$phase){
			$phase=(string)$phase;
			$status='pending';
			if($phase===$current_phase){
				$status='current';
			}
			elseif($phase===$next_phase){
				$status='next';
			}
			elseif($current_phase!=='' && array_search($phase, $allowed_phases, true)<array_search($current_phase, $allowed_phases, true)){
				$status='completed_or_skipped';
			}
			$timeline[]=[
				'order'=>$index+1,
				'phase'=>$phase,
				'status'=>$status,
				'tool'=>$this->mcp_workflow_phase_tool($phase),
			];
		}
		return [
			'export_type'=>'dataphyre_mcp_workflow_state_timeline_export',
			'write_policy'=>'read_only',
			'execution'=>'not_executed',
			'workflow'=>(string)($sync['workflow'] ?? 'generic'),
			'protocol'=>'2025-11-25',
			'state_id'=>$this->mcp_safe_handoff_label($sync['state_id'] ?? ''),
			'audit_passed'=>($sync['audit_passed'] ?? false)===true,
			'current_phase'=>$current_phase,
			'next_phase'=>$next_phase,
			'decision'=>(string)($sync['decision'] ?? ''),
			'recommended_tool'=>(string)($sync['recommended_tool'] ?? ''),
			'timeline'=>$timeline,
			'current'=>[
				'phase'=>$current_phase,
				'checkpoint_status'=>$sync['summary']['checkpoint_status'] ?? '',
				'audit_error_count'=>$sync['summary']['audit_error_count'] ?? 0,
				'audit_warning_count'=>$sync['summary']['audit_warning_count'] ?? 0,
			],
			'next'=>[
				'phase'=>$next_phase,
				'tool'=>$sync['recommended_tool'] ?? '',
				'patch'=>$sync['transition']['suggested_patch'] ?? [],
			],
			'application_agent_operating_contract'=>$this->mcp_application_agent_operating_contract('workflow_state_timeline'),
			'ordinary_app_work'=>$this->mcp_ordinary_app_work_contract('workflow_state_timeline'),
			'usage_notes'=>[
				'This timeline is derived from client-owned state and sync-pack guidance; it does not persist or mutate state.',
				'Use current and next for quick agent continuity, and use the sync pack when the full audit or patch detail is needed.',
				'Completed_or_skipped phases are inferred from phase order, not from server-side history.',
			],
		];
	}

