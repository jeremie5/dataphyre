		$workflow=strtolower(trim((string)($args['workflow'] ?? 'client')));
		if(!in_array($workflow, ['feature', 'routes', 'sql', 'diagnostics', 'client', 'release'], true)){
			$workflow='client';
		}
		$include_frames=($args['include_frames'] ?? true)!==false;
		$playbook=$this->mcp_workflow_playbook_export(['workflow'=>$workflow]);
		$readiness=$this->mcp_workflow_readiness_audit(['workflow'=>$workflow]);
		$session=$this->mcp_workflow_session_export(['workflow'=>$workflow, 'include_frames'=>$include_frames]);
		$schema=$this->mcp_workflow_transcript_schema_export(['workflow'=>$workflow]);
		return [
			'export_type'=>'dataphyre_mcp_workflow_handoff_pack_export',
			'write_policy'=>'read_only',
			'execution'=>'not_executed',
			'workflow'=>$workflow,
			'protocol'=>'2025-11-25',
			'transport'=>'stdio',
			'ready_to_run'=>($readiness['all_ready'] ?? false)===true,
			'include_frames'=>$include_frames,
			'playbook'=>$playbook,
			'readiness'=>$readiness,
			'session'=>$session,
			'transcript_schema'=>[
				'workflow'=>$schema['workflow'] ?? $workflow,
				'schema'=>$schema['schema'] ?? [],
				'required_redactions'=>$schema['required_redactions'] ?? [],
				'step_summary_rules'=>$schema['step_summary_rules'] ?? [],
			],
			'pre_run_checklist'=>[
				'Confirm ready_to_run is true before sending workflow messages.',
				'Send session_messages in order to the stdio server; do not send this pack itself as an MCP frame.',
				'Replace placeholder arguments with earlier step outputs before dependent calls.',
				'Capture responses using transcript_schema, then audit and summarize with transcript tools.',
			],
			'post_run_tools'=>[
				'dataphyre_mcp_workflow_transcript_audit',
				'dataphyre_mcp_workflow_transcript_summary_export',
				'dataphyre_mcp_safety_boundary_report',
			],
			'application_agent_operating_contract'=>$this->mcp_application_agent_operating_contract('workflow_handoff_pack'),
			'ordinary_app_work'=>$this->mcp_ordinary_app_work_contract('workflow_handoff_pack'),
			'tool_audience_boundaries'=>$this->mcp_current_tool_audience_boundaries(),
			'continuity_policy'=>$this->mcp_workflow_continuity_policy('workflow_handoff_pack'),
			'usage_notes'=>[
				'This handoff pack is pre-run guidance only; it does not execute or store workflow messages.',
				'The pack is generic and should not be edited to include product-local paths in shared docs or fixtures.',
				'Use focused application or module verification for app workflow results; use dataphyre_mcp_verify_all only for MCP/release-surface workflow claims.',
			],
		];
	}


